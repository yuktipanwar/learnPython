num1 = float(input("enter number one :"))
num2 = float(input("enter second one :"))
op= input("enter operator :")

if op=="+":
    print(num1+ num2)
elif op=="-":
    print(num1- num2)
elif op=="*":
    print(num1*num2)
elif op=="/":
    print(num1/num2)
else:
    print("invalid operator")




